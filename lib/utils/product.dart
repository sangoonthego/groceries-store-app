class Product {
  final String name;
  final String mass;
  final String price;
  final String image;

  Product({
    required this.name,
    required this.mass,
    required this.price,
    required this.image,
  });
}

class BeanProduct {
  final String name;
  final String image;

  BeanProduct({required this.name, required this.image});
}
